package com.loja.informatica.MODEL;

import java.util.Date;

/**
 *Classe desenvolvida contendo os atributos para se ter os dados do relatorio das vendas.
 * Contem tambem os metodos getters and setters para poder modificar os pegar os dados de suas variaveis.
 * @author Marcos Vin√≠cius Santos Souza
 */
public class Relatorios {
    
    private int idVenda;
    private String nome;
    private String cpf;
    private Date data;
    private double valorTotal;
    
    private String tipoModelo;
    private String descricao;
    private int quantidade;
    private double valorUnitario;

    public int getIdVenda() {
        return idVenda;
    }

    public void setIdVenda(int idVenda) {
        this.idVenda = idVenda;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }

    public double getValorTotal() {
        return valorTotal;
    }

    public void setValorTotal(double valorTotal) {
        this.valorTotal = valorTotal;
    }

    public String getTipoModelo() {
        return tipoModelo;
    }

    public void setTipoModelo(String tipoModelo) {
        this.tipoModelo = tipoModelo;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public int getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(int quantidade) {
        this.quantidade = quantidade;
    }

    public double getValorUnitario() {
        return valorUnitario;
    }

    public void setValorUnitario(double valorUnitario) {
        this.valorUnitario = valorUnitario;
    }
    
    
    
}
